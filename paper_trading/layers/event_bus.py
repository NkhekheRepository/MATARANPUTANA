@dataclass
class BaseEvent:
    """Base event class with trace_id and timestamp."""
    event_type: EventType
    trace_id: str = field(default_factory=lambda: str(uuid.uuid4()))
    timestamp: float = field(default_factory=time.time)
    source_layer: Optional[int] = None
    correlation_id: Optional[str] = None  # For grouping related events
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert event to dictionary for serialization."""
        data = asdict(self)
        data['event_type'] = self.event_type.value
        data['timestamp'] = self.timestamp
        return data
    
    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> 'BaseEvent':
        """Create event from dictionary - only includes BaseEvent fields."""
        data_copy = data.copy()
        if 'event_type' in data_copy and isinstance(data_copy['event_type'], str):
            data_copy['event_type'] = EventType(data_copy['event_type'])
        
        # Extract only BaseEvent fields
        base_fields = ['event_type', 'trace_id', 'timestamp', 'source_layer', 'correlation_id']
        filtered_data = {k: v for k, v in data_copy.items() if k in base_fields}
        return cls(**filtered_data)


