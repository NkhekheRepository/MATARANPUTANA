class EventBus:
    """
    Redis-based event bus with typed events and trace_id support.
    Provides publish/subscribe capabilities with event replay functionality.
    Thread-safe: all subscriber operations protected by RLock.
    """
    
    REDIS_HOST = os.environ.get("REDIS_HOST", "localhost")
    REDIS_PORT = int(os.environ.get("REDIS_PORT", "6379"))
    
    def __init__(self, redis_host: str = None, redis_port: int = None):
        self.redis_host = redis_host or self.REDIS_HOST
        self.redis_port = redis_port or self.REDIS_PORT
        self.redis_client: Optional[redis.Redis] = None
        self._subscribers: Dict[str, List[Callable]] = defaultdict(list)
        self._lock = threading.RLock()
        self._running = False
        self._listener_thread: Optional[threading.Thread] = None
        
        # Event storage for replay (last 1000 events per type) — always initialized
        self._memory_store: List[Dict] = []
        self.event_history_key = "fo:event_history"
        
        self._connect()
    
    def _connect(self) -> bool:
        """Connect to Redis."""
        try:
            self.redis_client = redis.Redis(
                host=self.redis_host,
                port=self.redis_port,
                decode_responses=True,
                socket_connect_timeout=5,
                socket_keepalive=True
            )
            self.redis_client.ping()
            logger.info(f"EventBus connected to Redis at {self.redis_host}:{self.redis_port}")
            return True
        except Exception as e:
            logger.warning(f"EventBus Redis connection failed: {e}. Using in-memory mode.")
            self.redis_client = None
            # Use in-memory storage as fallback
            self._memory_store: List[Dict] = []
            return False
    
    def disconnect(self):
        """Disconnect from Redis."""
        self._running = False
        if self._listener_thread:
            self._listener_thread.join(timeout=5)
        if self.redis_client:
            try:
                self.redis_client.close()
            except Exception:
                pass
            logger.info("EventBus disconnected from Redis")
    
    def publish(self, event: BaseEvent) -> bool:
        """
        Publish an event to the event bus.
        Returns True if successful, False otherwise.
        """
        try:
            event_dict = event.to_dict()
            event_data = json.dumps(event_dict, cls=EventJSONEncoder)
            
            # Add to event history for replay
            history_entry = {
                "trace_id": event.trace_id,
                "event_type": event.event_type.value,
                "timestamp": event.timestamp,
                "data": event_data
            }
            
            if self.redis_client:
                try:
                    # Add to Redis list (keep last 1000 events)
                    self.redis_client.lpush(self.event_history_key, event_data)
                    self.redis_client.ltrim(self.event_history_key, 0, 999)
                    
                    # Publish to channel for subscribers
                    channel = f"fo:event:{event.event_type.value}"
                    self.redis_client.publish(channel, event_data)
                except Exception as e:
                    logger.error(f"Redis publish error: {e}")
                    # Fall back to memory store
                    self._memory_store.append(history_entry)
                    if len(self._memory_store) > 1000:
                        self._memory_store = self._memory_store[:1000]
            else:
                # Use in-memory storage
                self._memory_store.append(history_entry)
                if len(self._memory_store) > 1000:
                    self._memory_store = self._memory_store[:1000]
            
            # Call local subscribers immediately (synchronous dispatch)
            channel = f"fo:event:{event.event_type.value}"
            with self._lock:
                subscribers = list(self._subscribers.get(channel, []))
            
            # Process all subscribers in threads to prevent blocking
            for i, callback in enumerate(subscribers):
                try:
                    import threading
                    result_holder = [None]
                    exception_holder: List[Optional[Exception]] = [None]
                    
                    def run_callback():
                        try:
                            result_holder[0] = callback(event)
                        except Exception as e:
                            exception_holder[0] = e
                    
                    t = threading.Thread(target=run_callback, daemon=True)
                    t.start()
                    t.join(timeout=5)
                    
                    if t.is_alive():
                        logger.warning(f"Callback {i} timed out after 5s")
                    elif exception_holder[0]:
                        raise exception_holder[0]
                except Exception as e:
                    logger.error(f"Error in callback {i}: {e}")
            
            return True
        
        except Exception as e:
            logger.error(f"Failed to publish event: {e}")
            return False
    
    def subscribe(self, event_types: List[EventType], callback: Callable) -> bool:
        """
        Subscribe to specific event types.
        Callback receives the deserialized event object.
        Thread-safe: subscriber registration protected by lock.
        """
        try:
            with self._lock:
                for event_type in event_types:
                    channel = f"fo:event:{event_type.value}"
                    self._subscribers[channel].append(callback)
                
                should_start = not self._running and self.redis_client
                if should_start:
                    self._running = True
            
            if should_start:
                self._listener_thread = threading.Thread(target=self._listen_loop, daemon=True)
                self._listener_thread.start()
            
            logger.info(f"Subscribed to {[et.value for et in event_types]}")
            return True
            
        except Exception as e:
            logger.error(f"Failed to subscribe to events: {e}")
            return False
    
    def _listen_loop(self):
        """Listen for events from Redis in background thread."""
        if not self.redis_client:
            return
        
        pubsub = self.redis_client.pubsub()
        
        with self._lock:
            channels = [ch for ch in self._subscribers.keys()]
        if channels:
            try:
                pubsub.subscribe(*channels)
            except Exception as e:
                logger.error(f"Failed to subscribe to Redis channels: {e}")
                return
        
        try:
            while self._running:
                try:
                    message = pubsub.get_message(ignore_subscribe_messages=True, timeout=1.0)
                    if message and message['type'] == 'message':
                        channel = message['channel']
                        data = message['data']
                        
                        try:
                            event_dict = json.loads(data)
                            event = BaseEvent.from_dict(event_dict)
                            
                            with self._lock:
                                callbacks = list(self._subscribers.get(channel, []))
                            for callback in callbacks:
                                try:
                                    callback(event)
                                except Exception as e:
                                    logger.error(f"Event callback error: {e}")
                                        
                        except json.JSONDecodeError as e:
                            logger.error(f"Failed to deserialize event: {e}")
                            
                except Exception as e:
                    logger.error(f"Error in event listener: {e}")
                    time.sleep(1)
                    
        except Exception as e:
            logger.error(f"Event listener stopped: {e}")
        finally:
            try:
                pubsub.unsubscribe()
                pubsub.close()
            except Exception:
                pass
    
    def get_event_by_trace_id(self, trace_id: str) -> Optional[BaseEvent]:
        """Retrieve an event by its trace_id for replay/debugging."""
        try:
            if self.redis_client:
                # Search through history (this is O(n) but acceptable for 1000 events)
                events = self.redis_client.lrange(self.event_history_key, 0, -1)
                for event_data in events:
                    event_dict = json.loads(event_data)
                    if event_dict.get('trace_id') == trace_id:
                        return BaseEvent.from_dict(event_dict)
            else:
                # Search memory store
                for entry in self._memory_store:
                    if entry.get('trace_id') == trace_id:
                        return BaseEvent.from_dict(json.loads(entry['data']))
            
            return None
            
        except Exception as e:
            logger.error(f"Failed to retrieve event by trace_id: {e}")
            return None
    
    def get_events_by_type(self, event_type: EventType, limit: int = 100) -> List[BaseEvent]:
        """Get recent events of a specific type for replay/analysis."""
        try:
            events = []
            
            if self.redis_client:
                event_data_list = self.redis_client.lrange(self.event_history_key, 0, limit * 2)
                for event_data in event_data_list[:limit]:
                    event_dict = json.loads(event_data)
                    if event_dict.get('event_type') == event_type.value:
                        events.append(BaseEvent.from_dict(event_dict))
            else:
                for entry in self._memory_store[:limit]:
                    if entry.get('event_type') == event_type.value:
                        events.append(BaseEvent.from_dict(json.loads(entry['data'])))
            
            return events[:limit]
            
        except Exception as e:
            logger.error(f"Failed to get events by type: {e}")
            return []
    
    def get_events_in_time_range(self, start_time: float, end_time: float) -> List[BaseEvent]:
        """Get events within a time range for replay/analysis."""
        try:
            events = []
            
            if self.redis_client:
                event_data_list = self.redis_client.lrange(self.event_history_key, 0, 1000)
                for event_data in event_data_list:
                    event_dict = json.loads(event_data)
                    timestamp = event_dict.get('timestamp', 0)
                    if start_time <= timestamp <= end_time:
                        events.append(BaseEvent.from_dict(event_dict))
            else:
                for entry in self._memory_store:
                    if start_time <= entry.get('timestamp', 0) <= end_time:
                        events.append(BaseEvent.from_dict(json.loads(entry['data'])))
            
            return sorted(events, key=lambda e: e.timestamp)
            
        except Exception as e:
            logger.error(f"Failed to get events in time range: {e}")
            return []
    
    def get_event_count(self) -> int:
        """Get total number of events stored."""
        if self.redis_client:
            return self.redis_client.llen(self.event_history_key)
        return len(self._memory_store)
    
    def clear_history(self):
        """Clear event history."""
        if self.redis_client:
            self.redis_client.delete(self.event_history_key)
        else:
            self._memory_store.clear()
        logger.info("Event history cleared")
    
    def health_check(self) -> Dict[str, Any]:
        """Check EventBus health status."""
        status = {
            "connected": False,
            "subscriber_count": len(self._subscribers),
            "event_count": self.get_event_count(),
            "running": self._running
        }
        
        if self.redis_client:
            try:
                self.redis_client.ping()
                status["connected"] = True
            except Exception:
                status["connected"] = False
        else:
            status["connected"] = False
            status["mode"] = "in-memory"
        
        return status


# Global event bus instance
_event_bus_instance: Optional[EventBus] = None
_event_bus_lock = threading.Lock()


def get_event_bus() -> EventBus:
    """Get singleton event bus instance. Thread-safe with double-checked locking."""
    global _event_bus_instance
    if _event_bus_instance is None:
        with _event_bus_lock:
            if _event_bus_instance is None:
                _event_bus_instance = EventBus()
    return _event_bus_instance


def reset_event_bus():
    """Reset the global event bus instance."""
    global _event_bus_instance
    if _event_bus_instance:
        _event_bus_instance.disconnect()
    _event_bus_instance = None


